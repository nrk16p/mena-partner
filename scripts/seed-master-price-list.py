def build_doc(row, now):
    return {
        "licensePlate":          row[0],
        "downPayment":           row[1],
        "cashDown":              row[2],
        "remainingInstallment":  row[3],
        "downInstallmentCount":  row[4],
        "downInstallmentAmt":    row[5],
        "financeInstallments":   row[6],
        "monthlyPayment":        row[7],
        "financeAmount":         row[8],
        "totalSalePrice":        row[9],
        "updatedAt":             now,
    }

